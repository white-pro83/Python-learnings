#Programa: Ejemplo caracteres especiales en Python

#Salto de linea \n
mensaje1 = "Hola\nMundo"
print(mensaje1)

# Tabulacion \t
print("12341234123412341234")
mensaje2 = "\tJuan Perez"
print(mensaje2)

# Comillas dentro de cadena
mensaje3 = "Ella dijo: \"Hola a todos\""
print(mensaje3)

#Barra invertida \
mensaje4 = "C:\\Usuarios\\Marce\\Documentos"
print(mensaje4)

# Cadenas cruda (raw string) r
mensaje5 = r"C:\Usuarios\Marce\Documentos"
print(mensaje5)